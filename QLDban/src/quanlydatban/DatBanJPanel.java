    private void hienThiLichDatBan() {
        dtmDatBan.setRowCount(0);
        dsDatBan = datBanService.layDanhSachDatBan();
        for (DatBan datBan : dsDatBan) {
            Vector<Object> vec = new Vector<>();
            vec.add(datBan.getBanSo());
            vec.add(datBan.gettenKhachHang());
            vec.add(datBan.getsdtKhachHang());
            vec.add(datBan.getthoigian());
            dtmDatBan.addRow(vec);
        }
    }
