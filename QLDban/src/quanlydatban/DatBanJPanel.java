    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        rowSelected = tblDatBan.getSelectedRow();
            if (rowSelected != -1) {
                datBanService.xoaLichDat(String.valueOf(dtmDatBan.getValueAt(rowSelected, 0)), String.valueOf(dtmDatBan.getValueAt(rowSelected, 1)));
                hienThiLichDatBan();
            } else {
                    JOptionPane.showMessageDialog(DatBanJPanel.this, "Không có gì để xóa!");
                
            }
    }//GEN-LAST:event_btnXoaActionPerformed

